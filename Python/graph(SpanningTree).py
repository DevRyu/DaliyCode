# 최소 신장 트리 (Minimum Spanning Tree)
# 원래의 그래프의 모든 노드가 연결되어 있으면서  트리의 속성을 만족

# 신장 트리가 되기 위한 조건 3가지
# 1) 본래의 그래프의 모든 노드를 포함해야 함
# 2) 모든 노드가 서로 연결
# 3) 트리의 속성을 만족시킴(사이클이 존재하지 않음)

# 최소 신장트리(Minimum Spanning Tree)
